# coordinate system:
#   x = แนวนอน  (0 = ซ้ายสุด)
#   y = แนวตั้ง (0 = ล่างสุด, ยิ่งมากยิ่งสูง)
#   walls = ทุก tile ที่งูเกาะได้ (พื้น + กำแพงด้านข้าง)
