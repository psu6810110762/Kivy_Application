# ฟังก์ชันช่วยสร้างพื้นยาวๆ จะได้ไม่ต้องพิมพ์พิกัดทีละตัว
def create_platform(x_start, x_end, y):
    return [(x, y) for x in range(x_start, x_end + 1)]

def create_wall(x, y_start, y_end):
    return [(x, y) for y in range(y_start, y_end + 1)]

