def make_platform(x1, x2, y1, y2):
    """สร้าง wall tiles สำหรับ platform สี่เหลี่ยม"""
    tiles = set()
    for x in range(x1, x2 + 1):
        for y in range(y1, y2 + 1):
            tiles.add((x, y))
    return tiles

